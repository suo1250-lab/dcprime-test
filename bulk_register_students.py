SUBJECT_MAP = {"과": "과학", "국": "국어", "수": "수학", "영": "영어", "탐독": "탐독"}

def parse_subject(class_name: str) -> str:
    parts = class_name.split(".")
    key = parts[1] if len(parts) > 1 else ""
    return SUBJECT_MAP.get(key, key)


def get_or_create_class(cur, class_name: str, grade: str, subject: str) -> int:
    cur.execute("SELECT id FROM classes WHERE name = %s", (class_name,))
    row = cur.fetchone()
    if row:
        return row[0]
    cur.execute(
        "INSERT INTO classes (name, grade, subject) VALUES (%s, %s, %s) RETURNING id",
        (class_name, grade, subject),
    )
    return cur.fetchone()[0]


def main():
    filtered = [c for c in CLASS_DATA if c["teacher"] not in EXCLUDED_TEACHERS]
    excluded_count = len(CLASS_DATA) - len(filtered)
    print(f"전체 반: {len(CLASS_DATA)}개 | 제외: {excluded_count}개 | 등록 대상: {len(filtered)}개")

    p = urlparse(DATABASE_URL)
    conn = psycopg2.connect(
        host=p.hostname, port=p.port or 5432,
        dbname=p.path.lstrip("/"), user=p.username, password=p.password,
    )
    cur = conn.cursor()

    try:
        cur.execute("SELECT name, grade, class_id FROM students WHERE class_id IS NOT NULL")
        existing = {(row[0], row[1], row[2]) for row in cur.fetchall()}
        print(f"기존 DB 학생-반 조합: {len(existing)}개")

        to_insert = []  # (name, grade, teacher, class_id)
        skipped = []
        seen = set(existing)

        for cls in filtered:
            if not cls["students"]:
                continue
            grade = cls["students"][0][1]
            subject = parse_subject(cls["class_name"])
            class_id = get_or_create_class(cur, cls["class_name"], grade, subject)

            for name, stu_grade in cls["students"]:
                key = (name, stu_grade, class_id)
                if key in seen:
                    skipped.append(f"{name}({stu_grade}/{cls['class_name']})")
                else:
                    to_insert.append((name, stu_grade, cls["teacher"], class_id))
                    seen.add(key)

        print(f"중복 제외: {len(skipped)}명")
        if skipped:
            print("  " + ", ".join(skipped))
        print(f"신규 등록 예정: {len(to_insert)}명")

        if not to_insert:
            print("등록할 학생이 없습니다.")
            conn.commit()
            return

        confirm = input(f"\n{len(to_insert)}명을 등록하시겠습니까? (yes/no): ")
        if confirm.strip().lower() != "yes":
            print("취소.")
            conn.rollback()
            return

        execute_values(
            cur,
            "INSERT INTO students (name, grade, teacher, class_id) VALUES %s",
            to_insert,
        )

        # 기존에 class_id 없이 등록된 학생 업데이트
        update_count = 0
        for cls in filtered:
            if not cls["students"]:
                continue
            grade = cls["students"][0][1]
            subject = parse_subject(cls["class_name"])
            class_id = get_or_create_class(cur, cls["class_name"], grade, subject)
            for name, stu_grade in cls["students"]:
                cur.execute(
                    "UPDATE students SET class_id = %s, teacher = %s WHERE name = %s AND grade = %s AND class_id IS NULL",
                    (class_id, cls["teacher"], name, stu_grade),
                )
                update_count += cur.rowcount

        conn.commit()
        print(f"\n✓ 신규 {len(to_insert)}명 등록 + 기존 {update_count}명 class_id 업데이트 완료!")

    except Exception as e:
        conn.rollback()
        print(f"오류: {e}")
        raise
    finally:
        cur.close()
        conn.close()


if __name__ == "__main__":
    main()
